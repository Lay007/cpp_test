#include "my_project/dsp.hpp"

#include <chrono>
#include <cmath>
#include <filesystem>
#include <fstream>
#include <functional>
#include <iomanip>
#include <iostream>
#include <numeric>
#include <stdexcept>
#include <string>
#include <vector>

namespace {

using Clock = std::chrono::steady_clock;
using Signal = my_project::dsp::Signal;

struct BenchmarkResult {
    std::string name;
    std::size_t samples{};
    int iterations{};
    double avg_ms{};
    double throughput_msa_s{};
    double checksum{};
};

BenchmarkResult run_benchmark(const std::string& name,
                              std::size_t samples,
                              int iterations,
                              const std::function<double()>& fn) {
    if (iterations <= 0) {
        throw std::invalid_argument("iterations must be > 0");
    }

    volatile double sink = 0.0;
    sink += fn();  // warm-up

    const auto start = Clock::now();
    for (int i = 0; i < iterations; ++i) {
        sink += fn();
    }
    const auto end = Clock::now();
    const std::chrono::duration<double, std::milli> elapsed_ms = end - start;
    const double avg_ms = elapsed_ms.count() / static_cast<double>(iterations);
    const double throughput = avg_ms > 0.0
                                  ? (static_cast<double>(samples) / 1e6) / (avg_ms / 1e3)
                                  : 0.0;

    return BenchmarkResult{
        name, samples, iterations, avg_ms, throughput, static_cast<double>(sink)};
}

void write_markdown_report(const std::vector<BenchmarkResult>& results,
                           const std::string& report_path,
                           int iterations) {
    std::filesystem::path path(report_path);
    if (path.has_parent_path()) {
        std::filesystem::create_directories(path.parent_path());
    }

    std::ofstream out(report_path, std::ios::out | std::ios::trunc);
    if (!out) {
        throw std::runtime_error("Unable to open benchmark report path: " + report_path);
    }

    out << "# DSP Performance Report\n\n";
    out << "Generated by `my_project_dsp_benchmark`.\n\n";
    out << "- Iterations per case: " << iterations << "\n";
    out << "- Build dependent results: yes (compare only same toolchain/config).\n\n";
    out << "| Benchmark | Samples | Avg time (ms) | Throughput (MSa/s) |\n";
    out << "|---|---:|---:|---:|\n";

    out << std::fixed << std::setprecision(3);
    for (const auto& r : results) {
        out << "| " << r.name << " | " << r.samples << " | " << r.avg_ms << " | "
            << r.throughput_msa_s << " |\n";
    }
}

}  // namespace

int main(int argc, char** argv) {
    int iterations = 15;
    std::string report_path;

    for (int i = 1; i < argc; ++i) {
        const std::string arg = argv[i];
        if (arg == "--iterations" && i + 1 < argc) {
            iterations = std::stoi(argv[++i]);
        } else if (arg == "--report" && i + 1 < argc) {
            report_path = argv[++i];
        } else if (arg == "--help") {
            std::cout << "Usage: my_project_dsp_benchmark [--iterations N] [--report PATH]\n";
            return 0;
        } else {
            std::cerr << "Unknown argument: " << arg << "\n";
            return 2;
        }
    }

    constexpr double kFs = 48'000.0;
    constexpr std::size_t kFirSamples = 32'768;
    constexpr std::size_t kGoertzelSamples = 8'192;
    constexpr std::size_t kGccSamples = 1'024;

    Signal fir_input = my_project::dsp::generate_tone(kFirSamples, kFs, 1'500.0, 1.0);
    Signal fir_noise = my_project::dsp::generate_tone(kFirSamples, kFs, 8'000.0, 0.25, 0.7);
    for (std::size_t i = 0; i < fir_input.size(); ++i) {
        fir_input[i] += fir_noise[i];
    }
    const auto fir_taps = my_project::dsp::design_lowpass_fir(127, 3'000.0 / kFs);

    Signal goertzel_signal = my_project::dsp::generate_tone(kGoertzelSamples, kFs, 2'000.0, 1.0);
    const auto goertzel_interferer =
        my_project::dsp::generate_tone(kGoertzelSamples, kFs, 5'500.0, 0.25, 0.2);
    for (std::size_t i = 0; i < goertzel_signal.size(); ++i) {
        goertzel_signal[i] += goertzel_interferer[i];
    }

    Signal reference = my_project::dsp::generate_tone(kGccSamples, kFs, 1'200.0, 0.9);
    const auto wideband = my_project::dsp::generate_tone(kGccSamples, kFs, 5'500.0, 0.35, 0.1);
    for (std::size_t i = 0; i < reference.size(); ++i) {
        reference[i] += wideband[i];
    }
    Signal delayed(kGccSamples, 0.0);
    constexpr int kDelay = 9;
    for (std::size_t i = static_cast<std::size_t>(kDelay); i < delayed.size(); ++i) {
        delayed[i] = reference[i - static_cast<std::size_t>(kDelay)];
    }

    std::vector<BenchmarkResult> results;
    results.push_back(run_benchmark("FIR 127-tap convolution", kFirSamples, iterations, [&]() {
        const auto out = my_project::dsp::convolve(fir_input, fir_taps);
        return std::accumulate(out.begin(), out.end(), 0.0);
    }));

    results.push_back(run_benchmark("Goertzel power detector", kGoertzelSamples, iterations, [&]() {
        const double p1 = my_project::dsp::goertzel_power(goertzel_signal, kFs, 2'000.0);
        const double p2 = my_project::dsp::goertzel_power(goertzel_signal, kFs, 5'500.0);
        return p1 - p2;
    }));

    results.push_back(run_benchmark("GCC-PHAT delay estimate", kGccSamples, iterations, [&]() {
        const auto r = my_project::dsp::estimate_delay_gcc_phat(reference, delayed, 64);
        return static_cast<double>(r.lag_samples) + r.score;
    }));

    std::cout << std::fixed << std::setprecision(3);
    std::cout << "DSP benchmark results (iterations=" << iterations << ")\n";
    for (const auto& r : results) {
        std::cout << " - " << r.name << ": avg=" << r.avg_ms << " ms, throughput="
                  << r.throughput_msa_s << " MSa/s\n";
    }

    if (!report_path.empty()) {
        write_markdown_report(results, report_path, iterations);
        std::cout << "Report written to: " << report_path << "\n";
    }

    return 0;
}
